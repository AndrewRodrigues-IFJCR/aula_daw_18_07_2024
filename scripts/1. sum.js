// 1. Escreva uma função que receba dois números como argumentos e retorne a soma deles.

/** 
 * Esta função soma dois números quaisquer.
 * @param {Number} firstNumber É o primeiro número a ser somado.
 * @param {Number} secondNumber É o segundo número a ser somado.
 * @returns {Number} 
*/

function sum(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}